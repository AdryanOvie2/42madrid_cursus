#include <string.h>

int	ft_strncmp(const char *s1, const char *s2, size_t count)
{
	while (count > 0 && (*s1 != '\0' || *s2 != '\0'))
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
		count--;
	}
}
